// Extension for 'Bool'
public extension Bool {
    public static func &&(lhs: Bool, rhs: Atomic<Bool>) -> Bool {
        return (lhs && rhs.value)
    }

    public static func &&(lhs: Atomic<Bool>, rhs: Bool) -> Bool {
        return (lhs.value && rhs)
    }

    public static func ||(lhs: Bool, rhs: Atomic<Bool>) -> Bool {
        return (lhs || rhs.value)
    }

    public static func ||(lhs: Atomic<Bool>, rhs: Bool) -> Bool {
        return (lhs.value || rhs)
    }
}

